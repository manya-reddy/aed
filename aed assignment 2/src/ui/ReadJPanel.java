    private void populateTable() {
      
    
    DefaultTableModel model =(DefaultTableModel) tblHouses.getModel();
    model.setRowCount(0);
    
    for (House hs : history.getHistory())
    {
        Object[] row=new Object[3];
        row[0]=hs;
        row[1]=hs.getHouselId();
        row[2]=hs.getCommunityId();
        
        model.addRow(row);
    }
       
    
    
    }
