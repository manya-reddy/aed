    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex=tblHouses.getSelectedRow();
        
        if(selectedRowIndex<0)
        {
           JOptionPane.showMessageDialog(this,"Please select a row to delete");
           return;
        }
        
        DefaultTableModel model =(DefaultTableModel) tblHouses.getModel();
        House selectedHouse = (House) model.getValueAt(selectedRowIndex, 0);
        
        history.deleteHouses(selectedHouse);
         JOptionPane.showMessageDialog(this,"Data has been deleted!");
         
         populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex=tblHouses.getSelectedRow();
        
        if(selectedRowIndex<0)
        {
           JOptionPane.showMessageDialog(this,"Please select a valid entry");
           return;
        }
        
        DefaultTableModel model =(DefaultTableModel) tblHouses.getModel();
        House selectedHouse = (House) model.getValueAt(selectedRowIndex, 0);
        
        txtHouseName.setText(selectedHouse.getHouseName());
        txtHouseId.setText(selectedHouse.getHouselId());
        txtCommunityId.setText(selectedHouse.getCommunityId());
    }//GEN-LAST:event_btnReadActionPerformed


